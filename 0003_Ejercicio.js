/*
 * Introduce un tipo un Ejemplo para tipo de primitivo y saca por consola el resultado.
 */

//Number

let numero = 42;
console.log(numero); // 42

//String

let cadena = "Hola Mundo!!";
console.log(cadena); // Hola Mundo!!

//Boolean

let verdadero = true;
let falso = false;
console.log(verdadero, falso); // true false

//Undefined

let indefinido;
console.log(indefinido); // undefined

//Null

let nulo = null;
console.log(nulo); // null

//BigInt

let numeroGrande = 1234567890123456789012345678901234567890n;
console.log(numeroGrande); // 1234567890123456789012345678901234567890n

//Symbol

let simbolo = Symbol("descripcionUnica");
console.log(simbolo); // Symbol(descripcionUnica)




